#include "libft.h"

int		ft_isnumber(char *str)
{
	int		i;

	if (!str)
		return (0);
	i = 0;
	if (str[i] == '-' && !str[i + 1])
		return (0);
	if (str[i] == '-')
		i++;
	while (str[i])
	{
		if (!ft_isdigit(str[i]))
			return (0);
		i++;
	}
	return (1);
}
