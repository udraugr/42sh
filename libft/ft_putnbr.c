#include "libft.h"

void	ft_putnbr(int nb)
{
	int a;
	int b;

	if (nb == -2147483648)
	{
		ft_putnbr(-214748364);
		ft_putchar('8');
	}
	else
	{
		if (nb < 0)
		{
			nb = nb * (-1);
			ft_putchar('-');
		}
		a = nb / 10;
		b = nb % 10;
		if (a > 0)
			ft_putnbr(a);
		ft_putchar(b + 48);
	}
}
