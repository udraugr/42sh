#include "libft.h"

int	ft_isxdigit(int c)
{
	unsigned char u;

	u = c;
	if ((u >= 'A' && u <= 'F') || (u >= 'a' && u <= 'f') ||
		(u >= '0' && u <= '9'))
		return (c);
	return (0);
}
